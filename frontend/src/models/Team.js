export class Team{
    constructor(id, dateCreated, logo, motto, name, passcode, leagues, matches, tournaments, players) {
    this.id = id;
    this.dateCreated = dateCreated;
    this.logo = logo;
    this.motto = motto;
    this.name = name;
    this.passcode = passcode;
    this.leagues = leagues;
    this.matches = matches;
    this.tournaments = tournaments;
    this.players = players;
    }
}